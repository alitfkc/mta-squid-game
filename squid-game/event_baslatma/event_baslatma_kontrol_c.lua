function eventbaslat()
    addEventHandler("onClientPreRender",root,event_calisma_render_kontrol)
    addEventHandler("onClientKey", root, eventte_tus_engelleme) --oyuncu eventteyse f1,f1,b,j v.b tuşları engellemek için
    addEventHandler("onClientPlayerWasted",localPlayer,olunce_eventten_cikma) -- oyuncu ölürse dataları alacağız.
    addEventHandler("onClientPlayerDamage",localPlayer,eventte_hasar_yeme) -- oyuncu eventteyse hasar yemeyecek
    setPedWeaponSlot(localPlayer,0)
    for k,v in pairs(yasaklanacak_kontroller) do 
        toggleControl(v,false)
    end
end
addEvent("eventi_aktif_et",true)
addEventHandler("eventi_aktif_et",localPlayer,eventbaslat)
