function evente_katil()
    if getElementData(localPlayer,"squid_game") then 
        mesaj("Zaten katıldın, oyunun başlamasını bekle!","warning")
    else
        triggerServerEvent("squid_game_katilma_istegi",localPlayer)
    end
end
addCommandHandler("sq",evente_katil)
addEvent("sq:katil",true)
addEventHandler("sq:katil",localPlayer,evente_katil)
