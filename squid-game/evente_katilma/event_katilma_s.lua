function event_istek_kabul_et()
    if eventdurum then 
        triggerClientEvent(source,"ust_yazi",source,"Oyun çoktan başladı.","error")
        return
    end
    if oyuncusayisi<=oyun_baslama_max_oyuncu_sayisi then
        setElementData(source,"squid_game",true)
        triggerClientEvent(source,"ust_yazi",source,"Oyuna başarıyla katıldın.","success")
        oyuncusayisi=oyuncusayisi+1
        table.insert(evente_katilan_oyuncular,oyuncusayisi,source)
        for k,v in pairs(getElementsByType("player")) do
            setElementData(v,"f1:sq:event:oyuncu:sayisi",oyuncusayisi)
        end
        if oyuncusayisi>=oyun_baslama_min_oyuncu_sayisi then
            event_baslatma()
        end
    else
        triggerClientEvent(source,"ust_yazi",source,"Oyuncu sınırı doldu.","error")
        return
    end

end
addEvent("squid_game_katilma_istegi",true)
addEventHandler("squid_game_katilma_istegi",root,event_istek_kabul_et)
function oyuncu_sayisi_data_olusturma()
    setElementData(source,"f1:sq:event:oyuncu:sayisi",oyuncusayisi)
end
addEventHandler("onPlayerJoin",root,oyuncu_sayisi_data_olusturma)
