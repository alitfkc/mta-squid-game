local panelacilmayacaklar={}
function girisyapma(_, hesap)
    if not afis_gosterme_durum then 
        return 
    end
    local hesapismi=getAccountName(hesap)
    for k,v in ipairs(panelacilmayacaklar) do 
        if v==hesapismi then
            return 
        end
    end
    table.insert(panelacilmayacaklar,hesapismi)
    triggerClientEvent("sq_event_afis_ac",source)
end
addEventHandler("onPlayerLogin",root,girisyapma)
